PROBLEM_PATTERNS = """
常见题型与解法对应：

数组/字符串：
  · 子数组/子串的极值        → 滑动窗口
  · 找两数之和               → 哈希表 / 双指针（有序）
  · 区间求和                 → 前缀和
  · 原地修改有序数组         → 双指针（快慢指针）
  · 原地删除/移动元素        → 双指针

链表：
  · 找中间节点 / 判环        → 快慢指针
  · 合并有序链表             → 哑节点 + 双指针
  · 反转链表                 → 三指针迭代 / 递归

树：
  · 路径相关                 → DFS 递归
  · 层级相关                 → BFS / 层序遍历
  · 从序列重建树             → 递归 + 哈希表定位
  · BST 第K小 / 验证 BST    → 中序遍历

图：
  · 最短路（无权）           → BFS
  · 最短路（带权非负）       → Dijkstra
  · 连通分量 / 合并集合     → 并查集 / DFS
  · 有向图环检测 / 拓扑排序 → DFS 三色标记 / Kahn BFS
  · 最小生成树               → Kruskal / Prim

排序/查找：
  · 有序数组查找             → 二分查找
  · 第K大/小元素             → 堆 / 快速选择
  · 值域上的最优化           → 答案二分

动态规划：
  · 最优子结构 + 重叠子问题  → DP
  · 区间上的决策             → 区间DP
  · 子集/状态枚举            → 状态压缩DP
  · 背包类问题               → 背包DP

设计：
  · O(1) 增删查              → 哈希表
  · O(1) 最值 + 插入删除     → 堆
  · O(1) 最近使用            → LRU = 双向链表 + 哈希表
  · 前缀查找 / 自动补全      → Trie

字符串：
  · 模式匹配                 → KMP / Rabin-Karp
  · 最长回文                 → Manacher / 中心扩展
  · 字符串前缀操作           → Trie
"""

