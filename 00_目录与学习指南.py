COMPLEXITY_CHEATSHEET = {
    "数组/字符串": {
        "随机访问": "O(1)",
        "头部插入/删除": "O(n)",
        "末尾插入（摊销）": "O(1)",
        "查找": "O(n)",
    },
    "链表": {
        "已知位置插入/删除": "O(1)",
        "查找": "O(n)",
        "反转": "O(n)",
    },
    "哈希表": {
        "查找/插入/删除（平均）": "O(1)",
        "查找/插入/删除（最坏）": "O(n)",
    },
    "二叉搜索树（平均）": {
        "查找/插入/删除": "O(log n)",
    },
    "堆": {
        "插入": "O(log n)",
        "弹出最值": "O(log n)",
        "查看最值": "O(1)",
        "建堆": "O(n)",
    },
    "排序算法": {
        "冒泡/选择/插入": "O(n²)",
        "归并/快速/堆排序": "O(n log n)",
        "计数/基数/桶排序": "O(n+k)",
    },
    "图遍历": {
        "DFS/BFS": "O(V+E)",
        "Dijkstra": "O((V+E) log V)",
        "Bellman-Ford": "O(VE)",
        "Floyd-Warshall": "O(V³)",
        "Kruskal": "O(E log E)",
        "Prim": "O((V+E) log V)",
    },
    "字符串": {
        "KMP": "O(n+m)",
        "Rabin-Karp（平均）": "O(n+m)",
        "Trie 操作": "O(m)（m为字符串长度）",
        "Manacher": "O(n)",
    },
}


def print_cheatsheet():
    print("=" * 60)
    print("时间复杂度速查表")
    print("=" * 60)
    for category, ops in COMPLEXITY_CHEATSHEET.items():
        print(f"\n【{category}】")
        for op, complexity in ops.items():
            print(f"  {op:30s} {complexity}")


